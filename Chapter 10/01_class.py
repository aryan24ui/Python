class Employee:
    language = "Py" # language and salary are class attribute as the directly belong to class
    salary = 1200000

aryan = Employee() #aryan is object
aryan.name = "Aryan"
print(aryan.name, aryan.language, aryan.salary)

rohan = Employee()
rohan.name = "Rohan Roro Robinson"
print(rohan.name ,rohan.language, rohan.salary) 

